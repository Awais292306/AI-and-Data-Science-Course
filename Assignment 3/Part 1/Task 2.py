def check_number(num):
    
    # Function to check whether a number is positive, negative or zero
    if num > 0:
        print("The number is Positive.")
    elif num < 0:
        print("The number is Negative.")
    else:
        print("The number is Zero.")

# Example 
check_number(10)    
check_number(-5)    
check_number(0)     
